        $param = [            
            "personaid"=> 21,
            "programaid"=> 1,
            "tipo_recursoid"=> 3,
            "proposito"=> "Para comprar Alimentos",
            "fecha_alta"=> "2018-02-02",
            "monto"=> 220.300,
            "observacion"=> "Actualmente sin trabajo"
        ];
        
        $I->sendPOST('/api/recursos',$param);
